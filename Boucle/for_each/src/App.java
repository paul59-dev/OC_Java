public class App {
    public static void main(String[] args) throws Exception {
        int[] array = new int[]{1, 2, 3, 4};

        /*
         * Boucle For each 
         * Plus besoin de faire un .legth pour parcourir un tableau
         */
        for(int number: array) {
            System.out.println(number);
        }
    }
}
